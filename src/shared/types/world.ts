/** Result of extracting a world region into a structure. On success `path` is a freshly written
 *  temp `.nbt` the renderer either opens as a tab or feeds to the Export As / jigsaw-split flow;
 *  `oversized` means the region exceeds a Structure-Block axis limit (offer jigsaw on save);
 *  `refusedChunks` counts columns in the box that couldn't be read (proto/pre-1.13), skipped. */
export type WorldExtractResult =
  | {
      ok: true;
      path: string;
      name: string;
      size: [number, number, number];
      oversized: boolean;
      blocks: number;
      blockEntities: number;
      entities: number;
      refusedChunks: number;
    }
  | { ok: false; error: string };

/** One 16Ă—16Ă—16 section of a render payload. `uniform` sections carry no grid â€” every cell is the
 *  palette index `fill` (a stone/air fill costs nothing over IPC). */
export interface ChunkSectionPayload {
  sectionY: number;
  /** 4096 indices into the column palette (YZX), or null when `uniform`. */
  blocks: Uint16Array | null;
  uniform: boolean;
  /** Palette index that fills a uniform section (ignored when `blocks` is present). */
  fill: number;
  /** Biome ids present in this section (4Ă—4Ă—4 quart resolution); absent/null when the chunk
   *  predates paletted biomes (or for synthesized sections â€” the edit compositor's). Drives
   *  the world cursor readout's biome line. */
  biomePalette?: string[] | null;
  /** 64 indices into `biomePalette` (YZX quarts); absent/null when uniform (one biome). */
  biomes?: Uint8Array | null;
}

/** A chunk column resolved to renderable data, streamed to the renderer's mesh worker. The palette
 *  is unified across the column's sections (deduped by block-state string) and already carries
 *  resolved models + texture keys + fallback colours â€” the same shape the structure mesh path
 *  consumes, so the geometry core is shared. Typed arrays cross IPC via structured clone. */
export interface ChunkRenderPayload {
  cx: number;
  cz: number;
  palette: PaletteEntry[];
  sections: ChunkSectionPayload[];
  /** Every texture key referenced by the palette (the renderer preloads these before meshing). */
  textureKeys: string[];
  /** MOTION_BLOCKING surface (world Y per column, 256 XZ) for the mid LOD, or null. */
  heightmap: Int16Array | null;
  /** Dominant biome grass/foliage tint (sRGB 0..1) applied to `tintindex` faces, or null (default
   *  green). Per-chunk, so foliage varies by biome without a full per-block colormap. */
  grassTint: [number, number, number] | null;
  /** Entities to draw (armor stands, item frames, mobs) resolved to render shapes â€” their texture
   *  keys are folded into `textureKeys`. Only drawn at the near LOD. Empty when the chunk has none. */
  entities: StructureEntity[];
  /** True when the chunk exists on disk but has no renderable (non-air) sections AND no entities. */
  empty: boolean;
}
