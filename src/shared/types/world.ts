/** One pending block edit at an ABSOLUTE world position, as it crosses IPC to the write path.
 *  `name`/`properties` are the NBT-shaped block state (main maps them to `{Name, Properties}`).
 *  Air (`minecraft:air`) is an edit like any other. */
export interface WorldEditBlock {
  x: number;
  y: number;
  z: number;
  name: string;
  properties?: Record<string, string>;
  /** Full block-entity NBT to attach at this cell (must carry `id`; coordinates are stamped
   *  from the edit position by the write path). Omitted â‡’ any existing block entity at the
   *  cell is removed â€” a changed block never keeps a stale chest/spawner record. */
  blockEntity?: Record<string, unknown>;
}

/** One entity to spawn into the world (Place structure into world Â§2.2 fidelity). Unlike block
 *  edits, entities aren't cell-keyed â€” they ride the apply payload as their own list. */
export interface WorldEntityEdit {
  /** Absolute world position (doubles) the entity lands at. */
  pos: [number, number, number];
  /** The entity compound (must carry `id`). `Pos` is stamped from `pos` and `UUID` is
   *  regenerated by the write path (a pasted copy must never duplicate a UUID). */
  nbt: Record<string, unknown>;
}

/** Result of opening a world-edit session. `lockExclusive` is true only where the OS gave a REAL
 *  mutual-exclusion guarantee on `session.lock` (Windows); on macOS/Linux the hold is advisory and
 *  the UI must surface a "make sure Minecraft is closed" caution. */
export interface WorldEditOpenResult {
  lockExclusive: boolean;
}

/** A chunk the write path REFUSED (proto chunk, unknown DataVersion, unreadable) â€” reported, never
 *  "best-effort" written. */
export interface WorldRefusedChunk {
  cx: number;
  cz: number;
  reason: string;
}

/** One backup set under `<save>/blockwright-backups/` (mirrors main's `BackupSet`). */
export interface WorldBackupInfo {
  id: string;
  createdMs: number;
  /** Save-root-relative paths of the backed-up files. */
  files: string[];
  bytes: number;
}

/** What a Save-to-World actually did (mirrors main's `WorldEditReport`, serializable). */
export interface WorldEditApplyResult {
  changedBlocks: number;
  /** Entities actually written into `entities/` region files (refused chunks not counted). */
  placedEntities: number;
  editedChunks: { cx: number; cz: number }[];
  /** Absolute paths of the region files rewritten (block + poi + entities). */
  regions: string[];
  /** The backup taken before this save touched new files (null = all already backed up). */
  backup: WorldBackupInfo | null;
  refused: WorldRefusedChunk[];
}

